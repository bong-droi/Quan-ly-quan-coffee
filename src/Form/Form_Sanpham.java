    private void table_monMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_monMouseClicked
        // TODO add your handling code here:
        int row = table_mon.getSelectedRow();
        if (row >= 0) {
            txt_mamon.setText(table_mon.getValueAt(row, 0).toString());
            txt_tenmon.setText(table_mon.getValueAt(row, 1).toString());
            txt_gia.setText(table_mon.getValueAt(row, 2).toString());
            txt_loaimon.setText(table_mon.getValueAt(row, 3).toString());
            txt_hinhanh.setText(table_mon.getValueAt(row, 4).toString());
            String imagePath= table_mon.getValueAt(row, 4).toString();
            ImageIcon icon = new ImageIcon(imagePath);

        // Resize ảnh cho vừa JLabel
        Image img = icon.getImage();
        Image scaledImg = img.getScaledInstance(label_anh_mon.getWidth(), label_anh_mon.getHeight(), Image.SCALE_SMOOTH);
        label_anh_mon.setIcon(new ImageIcon(scaledImg));
           
        }
    
    }//GEN-LAST:event_table_monMouseClicked

