    private void but_timloaimon_chomonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_timloaimon_chomonActionPerformed
       String maMon = txt_search_loaimon_chomon.getText().trim();
    if (maMon.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập mã loại để tìm!");
        return;
    }
    DefaultTableModel model = (DefaultTableModel) table_mon.getModel();
    model.setRowCount(0); // Xóa dòng cũ

    Mon_DAO dao = new Mon_DAO();
    List<Mon> list = dao.selectByMaLoai(maMon);

    for (Mon m : list) {
        Object[] row = new Object[] {
            m.getMa_Mon(),
            m.getTen_Mon(),
            m.getGia_Mon(),
            m.getMa_LM(),
            m.getHinhanh()
        };
        model.addRow(row);
    }
   
    }//GEN-LAST:event_but_timloaimon_chomonActionPerformed

    private void but_them_monActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_them_monActionPerformed
    String ma = txt_mamon.getText();
    String ten = txt_tenmon.getText();
    String giaStr = txt_gia.getText();
    String maLM = txt_loaimon.getText();
    String hinh = txt_hinhanh.getText();

    if (ma.isEmpty() || ten.isEmpty() || giaStr.isEmpty() || maLM.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin món!");
        return;
    }

    LoaiMon_DAO loaiDao = new LoaiMon_DAO();
    if (!loaiDao.existsById(maLM)) {
        JOptionPane.showMessageDialog(this, "Loại món không tồn tại! Vui lòng kiểm tra lại mã loại món.");
        return;
    }

    try {
        double gia = Double.parseDouble(giaStr);
        Mon_DAO dao = new Mon_DAO();
        dao.Insert(new Mon(ma, ten, gia, maLM, hinh));
        JOptionPane.showMessageDialog(this, "Thêm món thành công!");
        loadTableMon();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Giá món phải là số!");
    }
    }//GEN-LAST:event_but_them_monActionPerformed

    private void but_xoa_monActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_xoa_monActionPerformed
    String ma = txt_mamon.getText().trim();
    if (ma.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập mã món cần xóa!");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa món này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        Mon_DAO dao = new Mon_DAO();
        dao.Delete(ma);
        JOptionPane.showMessageDialog(this, "Xóa món thành công!");
        loadTableMon();
        
    }
    }//GEN-LAST:event_but_xoa_monActionPerformed

