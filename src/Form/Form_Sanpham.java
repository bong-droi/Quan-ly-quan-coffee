    public void loadTableMon() {
    Mon_DAO dao = new Mon_DAO();
    List<Mon> list = dao.selectAll();
    DefaultTableModel model = (DefaultTableModel) table_mon.getModel();
    model.setRowCount(0);
    for (Mon mon : list) {
        model.addRow(new Object[]{
            mon.getMa_Mon(), mon.getTen_Mon(), mon.getGia_Mon(), mon.getMa_LM(), mon.getHinhanh()
        });
    }
}
    public void loadTableLoaiMon() {
    LoaiMon_DAO dao = new LoaiMon_DAO();
    List<LoaiMon> list = dao.selectAll();
    DefaultTableModel model = (DefaultTableModel) table_loaimon.getModel();
    model.setRowCount(0);
    for (LoaiMon lm : list) {
        model.addRow(new Object[]{
            lm.getMa_LM(), lm.getTen_LM()
        });
    }
}
    
     

