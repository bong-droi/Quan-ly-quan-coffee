    private void but_add_mon_vao_billActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_add_mon_vao_billActionPerformed
         try {
        String mabill = Form_Order.ma_Bill;
        String mamon = Label_tenmon.getText().trim();  // Dùng Ma_Mon nếu có
        int soluong = Integer.parseInt(txt_soluong.getText().trim());
        double gia = Double.parseDouble(Label_gia.getText().trim());
        double thanhtien = soluong * gia;

        if (soluong <= 0) {
            JOptionPane.showMessageDialog(null, "Làm ơn nhập số lượng lớn hơn 0!");
            return;
        }

        // 1. Kiểm tra tồn kho nguyên liệu
        CheBien_DAO cheBienDAO = new CheBien_DAO();
        Nguyenlieu_DAO nguyenLieuDAO = new Nguyenlieu_DAO(); // Bạn phải có DAO này

        List<CheBien> dsNgLieu = cheBienDAO.selectByMaMon(mamon);
        for (CheBien cb : dsNgLieu) {
            double sl_can = cb.getSL_chebien() * soluong;
            int sl_ton = nguyenLieuDAO.getSoLuongTon(cb.getMa_Nglieu()); // Bạn phải viết hàm này

            if (sl_ton < sl_can) {
                JOptionPane.showMessageDialog(null,
                    "Nguyên liệu \"" + cb.getMa_Nglieu() + "\" không đủ trong kho!\nCần: " + sl_can + ", Hiện có: " + sl_ton);
                return;
            }
        }

        // 2. Nếu đủ: Thêm vào bill
        Order order = new Order();
        order.setMa_Bill(mabill);
        order.setMa_Mon(mamon);
        order.setSL_order(soluong);
        order.setGia_Mon(gia);
        order.settonggia_order(thanhtien);

        Order_DAO orderdao = new Order_DAO();
        orderdao.insert(order); 

        loadTable_OrderBill(); // Cập nhật bảng bill
        JOptionPane.showMessageDialog(null, "Đã cập nhật món vào bill!");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Vui lòng nhập số lượng hợp lệ!");
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Có lỗi xảy ra khi xử lý món!");
    }
    }//GEN-LAST:event_but_add_mon_vao_billActionPerformed

