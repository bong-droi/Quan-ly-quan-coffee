public void loadTableMon() {
    Mon_DAO dao = new Mon_DAO();
    List<Mon> list = dao.selectAll();
    DefaultTableModel model = (DefaultTableModel) table_mon.getModel();
    model.setRowCount(0);
    for (Mon mon : list) {
        model.addRow(new Object[]{
            mon.getMa_Mon(), mon.getTen_Mon(), mon.getGia_Mon(), mon.getMa_LM(), mon.getHinhanh()
        });
    }
}
public void loadTable_OrderBill() {
    DefaultTableModel model = (DefaultTableModel) table_oder_bill.getModel();
    model.setRowCount(0); // Xóa dữ liệu cũ nếu có

    String mabill = Form.Form_Order.ma_Bill; // Lấy mã bill từ label

    Order_DAO orderDAO = new Order_DAO();
    List<Order> dsOrder = orderDAO.selectByMaBill(mabill); // Hàm này bạn cần viết trong DAO

    for (Order order : dsOrder) {
        Object[] row = {
            order.getMa_Bill(),
            order.getMa_Mon(),
            order.getGia_Mon(),
            order.getSL_order(),
            order.gettonggia_order()
        };
        model.addRow(row);
    }
}
