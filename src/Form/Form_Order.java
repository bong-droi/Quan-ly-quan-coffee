    private void but_new_billActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_new_billActionPerformed
        // TODO add your handling code here:
      try {
        // 1. Lấy số bàn
        String soBanText = txt_chon_ban.getText().trim();
        if (soBanText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số bàn!");
            return;
        }

        int soBan = Integer.parseInt(soBanText);

        // 2. Kiểm tra bàn có trống không
        Ban_DAO banDao = new Ban_DAO();
        Ban ban = banDao.selectById(soBan);

        if (ban == null) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy bàn!");
            return;
        }

        if (!ban.getTrangthai().equalsIgnoreCase("Trống")) {
            JOptionPane.showMessageDialog(this, "Bàn đã có người sử dụng!");
            return;
        }

        // 3. Lấy nhân viên hiện tại
        String currentUser = Form_login.currentUser;
        if (currentUser == null || currentUser.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không xác định được người dùng hiện tại.");
            return;
        }

        Nhanvien_DAO nvDao = new Nhanvien_DAO();
        Nhanvien nv = nvDao.SelectByUser(currentUser);
        if (nv == null) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy thông tin nhân viên.");
            return;
        }

        // 4. Tạo mã hóa đơn theo thời gian + random
        String timeStr = new SimpleDateFormat("yyyyMMddHHmmss").format(new Date());
        String randomStr = String.format("%04d", new Random().nextInt(1000000));
         ma_Bill = "HD" + timeStr + randomStr;

        // 5. Tạo hóa đơn mới
        Bill bill = new Bill();
        bill.setMa_Bill(ma_Bill);
        bill.setBan(soBan);
        bill.setMa_NV(nv.getMa_NV());
        bill.setTrangThai("Chưa thanh toán");

        // Thời gian hiện tại
        bill.setDate_Bill(new java.sql.Timestamp(System.currentTimeMillis()));

        // 6. Lưu vào CSDL
        Bill_DAO billDao = new Bill_DAO();
        billDao.insert(bill);

        // 7. Đổi trạng thái bàn sang "Đang sử dụng"
        ban.setTrangthai("Đang sử dụng");
        banDao.update(ban);

        // 8. Mở form gọi món
        Form_Goi formGoi = new Form_Goi(); // truyền mã bill nếu cần
        formGoi.setVisible(true);
        this.dispose();

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Số bàn không hợp lệ!");
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi khi tạo hóa đơn mới!");
    }
    }//GEN-LAST:event_but_new_billActionPerformed

