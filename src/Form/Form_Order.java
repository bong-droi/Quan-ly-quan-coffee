    private void loadTable_ban(List<Ban> list) {
    DefaultTableModel model = (DefaultTableModel) table_ban.getModel();
    model.setRowCount(0); // clear table
    for (Ban ban : list) {
        model.addRow(new Object[]{ban.getMaban(), ban.getTrangthai()});
    }
}
    private void fillTableBill(List<Bill> list) {
    DefaultTableModel model = (DefaultTableModel) table_bill.getModel();
    model.setRowCount(0); // Xóa dữ liệu cũ trước khi đổ mới

    for (Bill b : list) {
        model.addRow(new Object[]{
            b.getMa_Bill(),
            b.getMa_NV(),
            b.getTongGia(),
            b.getBan(),
            b.getDate_Bill(),
            b.getTrangThai()
        });
    }
}


