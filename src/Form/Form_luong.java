    private void but_tim_manv_theothangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_tim_manv_theothangActionPerformed
         try {
        String maNV = txt_search_manv.getText().trim();
        int thang = jComboBox_thang.getSelectedIndex() + 1;
        int nam = Integer.parseInt((String) combobox_nam.getSelectedItem());

        if (maNV.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã nhân viên.");
            return;
        }

        LichLamViec_DAO dao = new LichLamViec_DAO();
        List<LichLamViec> listAll = dao.selectByMaNhanVien(maNV);
        List<LichLamViec> ketqua = new ArrayList<>();

        for (LichLamViec l : listAll) {
            Calendar cal = Calendar.getInstance();
            cal.setTime(l.getNgay());
            if (cal.get(Calendar.MONTH) + 1 == thang && cal.get(Calendar.YEAR) == nam) {
                ketqua.add(l);
            }
        }

        fillTable(ketqua);
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Lỗi khi tìm theo mã NV + tháng: " );
    }
    }//GEN-LAST:event_but_tim_manv_theothangActionPerformed

