    private void but_xoa_chebienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_xoa_chebienActionPerformed
        // TODO add your handling code here:
        int selectedRow = table_chebien.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xóa!");
        return;
    }

    String maMon = table_chebien.getValueAt(selectedRow, 0).toString();
    String maNguyenLieu = table_chebien.getValueAt(selectedRow, 1).toString();

    int confirm = JOptionPane.showConfirmDialog(this, 
        "Bạn có chắc chắn muốn xóa nguyên liệu này khỏi món?", "Xác nhận", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            CheBien_DAO dao =new CheBien_DAO();
            dao.delete(maMon, maNguyenLieu);
            JOptionPane.showMessageDialog(this, "Xóa thành công!");
            loadCheBienTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_but_xoa_chebienActionPerformed

    private void but_capnhat_chebienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_capnhat_chebienActionPerformed
        // TODO add your handling code here:
        String maMon = txt_mamonchebien.getText().trim();
    String maNguyenLieu = txt_manguyenlieuchebien.getText().trim();
    String soLuongStr = txt_soluong_chebien.getText().trim();

    if (maMon.isEmpty() || maNguyenLieu.isEmpty() || soLuongStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
        return;
    }

    try {
        int soLuong = Integer.parseInt(soLuongStr);
        CheBien cb = new CheBien(maMon, maNguyenLieu, soLuong);
        CheBien_DAO dao =new CheBien_DAO();
        dao.update(cb);
        JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
        loadCheBienTable();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Số lượng phải là số nguyên!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
    }
    }//GEN-LAST:event_but_capnhat_chebienActionPerformed

