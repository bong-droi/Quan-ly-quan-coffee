    private void but_search_mamonchebienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_search_mamonchebienActionPerformed
        // TODO add your handling code here:
        String keyword = txt_mamonchebien.getText().trim();
         if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập từ khóa để tìm!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
         CheBien_DAO dao =new CheBien_DAO();
    List<CheBien> dsCB = dao.selectByMaMon(keyword);
    DefaultTableModel model = (DefaultTableModel) table_chebien.getModel();
    model.setRowCount(0); // Xoá dữ liệu cũ
    
    for (CheBien cb : dsCB) {
        model.addRow(new Object[]{
            cb.getMa_Mon(),
            cb.getMa_Nglieu(),
            cb.getMa_Nglieu()
        });
    }
    }//GEN-LAST:event_but_search_mamonchebienActionPerformed

    private void but_search_manglieuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_search_manglieuActionPerformed
        // TODO add your handling code here:
    String keyword = txt_search_manglieu.getText().trim();
        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập từ khóa để tìm!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Nguyenlieu_DAO dao = new Nguyenlieu_DAO();
        Nguyenlieu nl = dao.selectById(keyword);
        DefaultTableModel model = (DefaultTableModel) table_nguyenlieu.getModel();
        model.setRowCount(0);
        if (nl != null) {
            model.addRow(new Object[]{
                nl.getMa_Nglieu(), nl.getTen_Nglieu(), nl.getSL_NL(), nl.getGia_Nglieu(), nl.getSL_NLin(), nl.getDate_Nglieu()
            });
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy nguyên liệu!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_but_search_manglieuActionPerformed

    private void but_search_monActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_search_monActionPerformed
        // TODO add your handling code here:
        String maMon = txt_search_mamon.getText().trim();
    if (maMon.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập mã món để tìm!");
        return;
    }

    Mon_DAO dao = new Mon_DAO();
    Mon mon = dao.selectByMa_Mon(maMon);

    DefaultTableModel model = (DefaultTableModel) table_mamon.getModel();
    model.setRowCount(0); // Xóa dữ liệu cũ

    if (mon != null) {
        model.addRow(new Object[]{
            mon.getMa_Mon(),
            mon.getTen_Mon(),
            mon.getGia_Mon(),
            mon.getMa_LM(),
            mon.getHinhanh()
        });
    } else {
        JOptionPane.showMessageDialog(this, "Không tìm thấy món!");
    }
    }//GEN-LAST:event_but_search_monActionPerformed

