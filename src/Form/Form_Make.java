    private void loadTable_Mon() {
    DefaultTableModel model = (DefaultTableModel) table_mamon.getModel();
    model.setRowCount(0); // Xoá dữ liệu cũ
    Mon_DAO dao = new Mon_DAO();
    List<Mon> dsMon = dao.selectAll();
    for (Mon m : dsMon) {
        model.addRow(new Object[]{m.getMa_Mon(), m.getTen_Mon(),m.getGia_Mon()});
    }
}
    private void loadCheBienTable() {
    CheBien_DAO dao= new CheBien_DAO();
    List<CheBien> ds = dao.selectAll();
    DefaultTableModel model = (DefaultTableModel) table_chebien.getModel();
    model.setRowCount(0);
    for (CheBien cb : ds) {
        model.addRow(new Object[]{cb.getMa_Mon(), cb.getMa_Nglieu(), cb.getSL_chebien()});
    }
}
    private void loadTable_NguyenLieu() {
    DefaultTableModel model = (DefaultTableModel) table_nguyenlieu.getModel();
    model.setRowCount(0); // Xoá dữ liệu cũ
    Nguyenlieu_DAO dao= new Nguyenlieu_DAO();
    List<Nguyenlieu> dsNL = dao.selectAll();
    for (Nguyenlieu nl : dsNL) {
        model.addRow(new Object[]{nl.getMa_Nglieu(), nl.getTen_Nglieu(),nl.getSL_NL()});
    }
}


