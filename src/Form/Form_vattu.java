    public void loadTable() {
    Vattu_DAO dao = new Vattu_DAO();
    List<Vattu> list = dao.selectAll();
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    for (Vattu vt : list) {
        model.addRow(new Object[]{
            vt.getMat_VT(),
            vt.getTen_VT(),
            vt.getSL_VT(),
            vt.getGia_VT(),
            vt.getSl_hong(),
            vt.getNgay_nhap()
        });
    }
}

