    private void Button_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_searchActionPerformed
        String maVT = text_search.getText().trim();

    if (maVT.isEmpty()) {
        
        return;
    }

    Vattu_DAO dao = new Vattu_DAO();
    Vattu vt = dao.findByMaVT(maVT);

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Xoá toàn bộ dữ liệu cũ trên bảng

    if (vt != null) {
        model.addRow(new Object[]{
            vt.getMat_VT(),
            vt.getTen_VT(),
            vt.getSL_VT(),
            vt.getGia_VT(),
            vt.getSl_hong()
        });
    } else {
        JOptionPane.setText("Không tìm thấy vật tư với mã " );
    }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_Button_searchActionPerformed

    private void but_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_themActionPerformed
       try {
        Vattu vt = new Vattu(
            txtMaVT.getText(),
            txt_TenVT.getText(),
            Integer.parseInt(txt_SLVT.getText()),
            Double.parseDouble(txt_GiaVT.getText()),
            Integer.parseInt(txt_SLHong.getText()),
            new java.text.SimpleDateFormat("yyyy-MM-dd").parse(txt_NgayNhap.getText())
        );
        new Vattu_DAO().Insert(vt);
        JOptionPane.setText( "Thêm vật tư thành công!");
        loadTable(); // Cập nhật lại bảng
    } catch (Exception e) {
        JOptionPane.setText( "Lỗi thêm vật tư: " + e.getMessage());
    }     
    }//GEN-LAST:event_but_themActionPerformed

