    private void loadTableData() {
    try {
        // Tạo đối tượng DAO và lấy danh sách nhân viên từ cơ sở dữ liệu
        Nhanvien_DAO dao = new Nhanvien_DAO();
        ArrayList<Nhanvien> list = dao.SelectAll();  // Lấy danh sách nhân viên

        // Tạo một đối tượng DefaultTableModel để thêm dữ liệu vào bảng
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        // Xóa tất cả các dòng hiện có trong bảng trước khi thêm mới
        model.setRowCount(0);

        // Thêm dữ liệu nhân viên vào bảng
        for (Nhanvien nv : list) {
            Object[] row = new Object[]{
                nv.getMa_NV(),
                nv.getTen_NV(),
                nv.getCCCD(),
                nv.getSDT(),
                nv.getBirth(),
                nv.getGT() ? "Nam" : "Nữ",  // Kiểm tra giới tính
                nv.getRole() ? "Admin" : "Nhân viên",  // Kiểm tra quyền
                nv.getluong(),
                
                nv.getUser(),nv.getPass()
               
            };
            model.addRow(row);  // Thêm dòng vào bảng
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Có lỗi khi tải dữ liệu: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
    }
}
