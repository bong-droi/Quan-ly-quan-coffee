    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
         int row = jTable1.getSelectedRow();
    
    if (row != -1) {
        // Lấy dữ liệu từ các cột trong bảng và hiển thị vào các trường nhập liệu
        txt_Ma_Nv.setText(jTable1.getValueAt(row, 0).toString());  // Mã nhân viên
        txt_ten.setText(jTable1.getValueAt(row, 1).toString());     // Tên nhân viên
        txt_CCCD.setText(jTable1.getValueAt(row, 2).toString());    // CCCD
        txt_sdt.setText(jTable1.getValueAt(row, 3).toString());     // SĐT
        txt_Birth.setText(jTable1.getValueAt(row, 4).toString());   // Ngày sinh
        combobox_gioitinh.setSelectedItem(jTable1.getValueAt(row, 5).toString());  // Giới tính
        combobox_role.setSelectedItem(jTable1.getValueAt(row, 6).toString());     // Vai trò
        txt_Luong.setText(jTable1.getValueAt(row, 7).toString());   // Lương
        
        txt_user.setText(jTable1.getValueAt(row,8).toString());// Ca làm việc
        txt_pass.setText(jTable1.getValueAt(row,9).toString());
        
    }
    }//GEN-LAST:event_jTable1MouseClicked

