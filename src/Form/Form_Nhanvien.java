    private void but_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_updateActionPerformed
        try {
        // Lấy thông tin từ các trường nhập liệu
        String maNV = txt_Ma_Nv.getText();
        String tenNV = txt_ten.getText();
        String cccd = txt_CCCD.getText();
        String sdt = txt_sdt.getText();
        String birthstr = txt_Birth.getText();
        boolean gioitinh = combobox_gioitinh.getSelectedItem().toString().equals("Nam");
        boolean role = combobox_role.getSelectedItem().toString().equals("Admin");
        double luong = Double.parseDouble(txt_Luong.getText());
        
        String user = txt_user.getText();
        String pass = txt_pass.getText();

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date birth = sdf.parse(birthstr);

        // Tạo đối tượng nhân viên
        Nhanvien nv = new Nhanvien(maNV, tenNV, cccd, sdt, birth, gioitinh, role, luong,  user, pass);
        Nhanvien_DAO dao = new Nhanvien_DAO();
        
        // Gọi hàm cập nhật
        dao.Update(nv);

        // Làm mới bảng
        loadTableData();

        // Hiển thị thông báo
        JOptionPane.showMessageDialog(this, "Cập nhật thông tin nhân viên thành công!");
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Có lỗi xảy ra khi cập nhật: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_but_updateActionPerformed

