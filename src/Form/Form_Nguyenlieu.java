    private void but_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_themActionPerformed
        // TODO add your handling code here:
        String ma = txt_Manguyenlieu.getText().trim();
        String ten = txt_tennguyenlieu.getText().trim();
        String giaStr = txt_gia.getText().trim();

        if (ma.isEmpty() || ten.isEmpty() || giaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        double gia;
        try {
            gia = Double.parseDouble(giaStr);
            if (gia < 0) {
                JOptionPane.showMessageDialog(this, "Giá nguyên liệu không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Giá phải là số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Nguyenlieu nl = new Nguyenlieu(ma, ten, 0, gia, 0, new java.util.Date());
        Nguyenlieu_DAO dao = new Nguyenlieu_DAO();
        dao.insert(nl);
        JOptionPane.showMessageDialog(this, "Thêm nguyên liệu thành công!");
        loadTable();
    }//GEN-LAST:event_but_themActionPerformed

    private void but_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_xoaActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nguyên liệu cần xóa!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xóa?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String ma = jTable1.getValueAt(row, 0).toString();
            Nguyenlieu_DAO dao = new Nguyenlieu_DAO();
            dao.delete(ma);
            JOptionPane.showMessageDialog(this, "Xóa thành công!");
            loadTable();
        }
    }//GEN-LAST:event_but_xoaActionPerformed

    private void but_capnhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_capnhatActionPerformed
        // TODO add your handling code here:
        if (txt_Manguyenlieu.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nguyên liệu cần cập nhật từ bảng!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String ma = txt_Manguyenlieu.getText().trim();
        String ten = txt_tennguyenlieu.getText().trim();
        String giaStr = txt_gia.getText().trim();

        if (ten.isEmpty() || giaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin để cập nhật!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        double gia;
        try {
            gia = Double.parseDouble(giaStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Giá phải là số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Nguyenlieu nl = new Nguyenlieu(ma, ten, 0, gia, 0, new java.util.Date());
        Nguyenlieu_DAO dao = new Nguyenlieu_DAO();
        dao.update(nl);
        JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
        loadTable();
    }//GEN-LAST:event_but_capnhatActionPerformed

