    private void loadTable() {
    DefaultTableModel model = new DefaultTableModel();
    model.setColumnIdentifiers(new String[] { "Mã", "Tên", "Số lượng", "Giá", "Số lượng tồn", "Ngày nhập" });

    Nguyenlieu_DAO dao = new Nguyenlieu_DAO();
    List<Nguyenlieu> list = dao.selectAll();

    for (Nguyenlieu nl : list) {
        model.addRow(new Object[] {
            nl.getMa_Nglieu(),
            nl.getTen_Nglieu(),
            nl.getSL_NL(),
            nl.getGia_Nglieu(),
            nl.getSL_NLin(),
            nl.getDate_Nglieu()
        });
    }

    jTable1.setModel(model);
}

