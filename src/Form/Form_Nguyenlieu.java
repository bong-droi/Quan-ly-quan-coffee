    private void but_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_searchActionPerformed
        // TODO add your handling code here:
        String keyword = txt_search.getText().trim();
        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập từ khóa để tìm!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Nguyenlieu_DAO dao = new Nguyenlieu_DAO();
        Nguyenlieu nl = dao.selectById(keyword);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        if (nl != null) {
            model.addRow(new Object[]{
                nl.getMa_Nglieu(), nl.getTen_Nglieu(), nl.getSL_NL(), nl.getGia_Nglieu(), nl.getSL_NLin(), nl.getDate_Nglieu()
            });
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy nguyên liệu!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_but_searchActionPerformed

