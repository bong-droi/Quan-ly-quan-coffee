    private void loadTableBill(List<Bill> list) {
    DefaultTableModel model = (DefaultTableModel) table_bill.getModel();
    model.setRowCount(0);

    double tong = 0;
    for (Bill b : list) {
        model.addRow(new Object[]{
            b.getMa_Bill(),
            b.getMa_NV(),
            b.getTongGia(),
            b.getBan(),
            b.getDate_Bill()
            
        });
        if ("Đã thanh toán".equalsIgnoreCase(b.getTrangThai())) {
            tong += b.getTongGia();
        }
    }
    Label_tongtien_bill.setText( tong + " VNĐ");
}
    private void loadTableNhapnguyenlieu(List<Nhapnguyenlieu> list) {
    DefaultTableModel model = (DefaultTableModel) table_nhapnguyenlieu.getModel();
    model.setRowCount(0);

    double tong = 0;
    for (Nhapnguyenlieu nl : list) {
        model.addRow(new Object[]{
            nl.getMaNhap(),
            nl.getMaNguyenlieu(),
            nl.getSoLuong(),
            nl.getGia(),
            nl.getTongTien(),
            nl.getNgayNhap()
        });
        tong += nl.getSoLuong() * nl.getGia();
    }
    Label_tongtien_nhapnglieu.setText( tong + " VNĐ");
}
    private void loadTableHaohut(List<Haohut> list) {
    DefaultTableModel model = (DefaultTableModel) table_hoahut.getModel();
    model.setRowCount(0);

    double tong = 0;
    for (Haohut h : list) {
        model.addRow(new Object[]{
            h.getMa_Hao(),
            h.getMa_Vattu(),            
            h.getSL_VT(),
            h.getGia_VT(),
            h.getTonggia_VT(),
            h.getdate_invt()
        });
        tong += h.getTonggia_VT();
    }
    Label_tongtien_haophi.setText( tong + " VNĐ");
}
    private void loadTableChitiethoadon(List<Order> list) {
    DefaultTableModel model = (DefaultTableModel) table_chitiethoadon.getModel();
    model.setRowCount(0);

    for (Order ct : list) {
        Object[] row = new Object[] {
            ct.getMa_Bill(),
            ct.getMa_Mon(),
            ct.getSL_order(),
            ct.gettonggia_order()
        };
        model.addRow(row);
    }
}




