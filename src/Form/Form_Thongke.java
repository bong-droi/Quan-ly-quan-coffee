    private void but_search_ma_nvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_search_ma_nvActionPerformed
        // TODO add your handling code here:
        String maNV = txt_search_manv.getText().trim(); // Lấy dữ liệu người dùng nhập

    if (maNV.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập mã nhân viên để tìm kiếm.");
        return;
    }

    Bill_DAO billDAO = new Bill_DAO();
    List<Bill> list = billDAO.selectByMaNV(maNV);

    if (list.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Không tìm thấy hóa đơn nào của nhân viên này.");
    }

    loadTableBill(list); // Hiển thị danh sách hóa đơn tìm được lên bảng
    }//GEN-LAST:event_but_search_ma_nvActionPerformed

