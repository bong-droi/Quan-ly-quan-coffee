    private void but_search_theongayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_search_theongayActionPerformed
           try {
        int ngay = Integer.parseInt(combobox_ngay.getSelectedItem().toString());
        int thang = Integer.parseInt(combobox_thang.getSelectedItem().toString()) - 1; // Tháng trong Java bắt đầu từ 0
        int nam = Integer.parseInt(combobox_nam.getSelectedItem().toString());

        Calendar calendar = Calendar.getInstance();
        calendar.set(nam, thang, ngay);
        java.util.Date date = calendar.getTime();
        

        Bill_DAO dao = new Bill_DAO();
        List<Bill> list = dao.selectByDate(date);
        loadTableBill(list); // Phương thức đổ dữ liệu ra bảng'
        
        Nhapnguyenlieu_DAO nhap= new Nhapnguyenlieu_DAO();             
        List<Nhapnguyenlieu> list_nhap = nhap.selectByDate(date);
        loadTableNhapnguyenlieu(list_nhap);
        Haohut_DAO hao = new Haohut_DAO();
        List<Haohut> list_hao = hao.selectByDate(date);
        loadTableHaohut(list_hao);
        
        

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn ngày hợp lệ!");
    }
    }//GEN-LAST:event_but_search_theongayActionPerformed

    private void but_search_theothangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_search_theothangActionPerformed
        // TODO add your handling code here:
        try {
        int thang = Integer.parseInt(combobox_thang.getSelectedItem().toString());
        int nam = Integer.parseInt(combobox_nam.getSelectedItem().toString());

        // Bill
        Bill_DAO billDAO = new Bill_DAO();
        List<Bill> billList = billDAO.selectByMonth(thang, nam);
        loadTableBill(billList);

        // Nhập nguyên liệu
        Nhapnguyenlieu_DAO nlDAO = new Nhapnguyenlieu_DAO();
        List<Nhapnguyenlieu> nlList = nlDAO.selectByMonthYear(thang, nam);
        loadTableNhapnguyenlieu(nlList);

        // Hao hụt
        Haohut_DAO haoDAO = new Haohut_DAO();
        List<Haohut> haoList = haoDAO.selectByMonthYear(thang, nam);
        loadTableHaohut(haoList);

    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_but_search_theothangActionPerformed

