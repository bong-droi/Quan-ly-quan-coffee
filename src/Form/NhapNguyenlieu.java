    private void but_nhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_nhapActionPerformed
       // TODO add your handling code here:
try {
    String maNL = txt_Manguyenlieu.getText().trim();
    double soLuong = Integer.parseInt(txt_Soluong.getText().trim());

    // Lấy ngày hiện tại từ máy
    java.util.Date ngayNhap = new java.util.Date();

    // Lấy giá nguyên liệu từ DAO
    Nguyenlieu_DAO nlDao = new Nguyenlieu_DAO();
    double gia = nlDao.getGiaByMaNguyenlieu(maNL);
    if (gia <= 0) {
        JOptionPane.showMessageDialog(this, "Không tìm thấy nguyên liệu hoặc giá không hợp lệ.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return;
    }

    double tongTien = gia * soLuong;

    // Tạo đối tượng nhập nguyên liệu
    Nhapnguyenlieu_DAO dao = new Nhapnguyenlieu_DAO();
    Nhapnguyenlieu n = new Nhapnguyenlieu();
    n.setMaNhap(UUID.randomUUID().toString().substring(0, 8)); // Tạo mã ngẫu nhiên
    n.setMaNguyenlieu(maNL);
    n.setGia(gia);
    n.setTongTien(tongTien);
    n.setNgayNhap(ngayNhap);
    n.setSoLuong(soLuong);

    dao.insert(n);  // Lưu vào bảng nhập

    nlDao.capNhatSoLuong(maNL, soLuong); // Cập nhật SL_NL trong bảng nguyên liệu

    JOptionPane.showMessageDialog(this, "Nhập nguyên liệu thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    
} catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_but_nhapActionPerformed

